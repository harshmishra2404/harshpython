# What are Python Built-in modules?
# Python built-in modules are a set of libraries that come
# pre-installed with the Python installation. It will become
# a tedious task to install any required modules in between the
# process of development that’s why Python comes with some
# most used modules required. These modules provide a wide range of
# functionalities, from file operations and system-related tasks to mathematical
# computations and web services. The use of these modules simplifies the
# development process, as developers can leverage the built-in functions and
# classes for common tasks, providing code reusability, and efficiency.
# Some examples of Python built-in modules include “os”, “sys”, “math”, and “datetime”

